# funciones

# Definir una función que suma dos números
def suma(a, b):
    resultado = a + b
    return resultado

# Llamar a la función con valores específicos
resultado_suma = suma(3, 5)
print("El resultado de la suma es:", resultado_suma)


# Definir una función que imprime un saludo
def saludar(nombre):
    print("¡Hola,", nombre, "!")

# Llamar a la función
saludar("Juan")


