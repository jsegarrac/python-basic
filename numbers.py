# Trabajando con números
numero1 = 10
numero2 = 5

# Suma
suma = numero1 + numero2
print("La suma de", numero1, "y", numero2, "es:", suma)

# Resta
resta = numero1 - numero2
print("La resta de", numero1, "y", numero2, "es:", resta)

# Multiplicación
multiplicacion = numero1 * numero2
print("El resultado de multiplicar", numero1, "por", numero2, "es:", multiplicacion)

# División
division = numero1 / numero2
print("El resultado de dividir", numero1, "por", numero2, "es:", division)

# Trabajando con strings
nombre = "Juan"
apellido = "Perez"

# Concatenación de strings
nombre_completo = nombre + " " + apellido
print("Nombre completo:", nombre_completo)

# Accediendo a caracteres individuales
primer_caracter = nombre[0]
print("Primer caracter del nombre:", primer_caracter)

# Longitud de un string
longitud_apellido = len(apellido)
print("Longitud del apellido:", longitud_apellido)

# Trabajando con listas
numeros = [1, 2, 3, 4, 5]
numeros2 = [1, 2, 3, 4, 5]

# Accediendo a elementos individuales
primer_numero = numeros[0]
print("Primer número en la lista:", primer_numero)

# Modificando elementos de la lista
numeros[0] = 10
print("Lista de números después de modificar el primer elemento:", numeros)

# Agregando elementos a la lista
numeros.append(6)
print("Lista de números después de agregar un elemento:", numeros)

# Eliminando elementos de la lista
numeros.remove(3)
print("Lista de números después de eliminar el número 3:", numeros)

# Longitud de una lista
longitud_numeros = len(numeros)
print("Longitud de la lista de números:", longitud_numeros)

# Duplicar una lista
print(numeros * 2)

# Concatenar lsitas
print(numeros + numeros2)

