# Operaciones con números enteros
num_entero_1 = 10
num_entero_2 = 5

# Suma
suma = num_entero_1 + num_entero_2  # Resultado: 15

# Resta
resta = num_entero_1 - num_entero_2  # Resultado: 5

# Multiplicación
multiplicacion = num_entero_1 * num_entero_2  # Resultado: 50

# División (división entera)
division_entera = num_entero_1 // num_entero_2  # Resultado: 2

# Módulo (resto de la división)
modulo = num_entero_1 % num_entero_2  # Resultado: 0

# Potenciación
potencia = num_entero_1 ** num_entero_2  # Resultado: 100000

# Operaciones con números de coma flotante
num_float_1 = 3.5
num_float_2 = 2.0

# Suma
suma_float = num_float_1 + num_float_2  # Resultado: 5.5

# Resta
resta_float = num_float_1 - num_float_2  # Resultado: 1.5

# Multiplicación
multiplicacion_float = num_float_1 * num_float_2  # Resultado: 7.0

# División (división flotante)
division_float = num_float_1 / num_float_2  # Resultado: 1.75

# Potenciación
potencia_float = num_float_1 ** num_float_2  # Resultado: 12.25

