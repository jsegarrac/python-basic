# Comparaciones con numeros, para strings es muy parecido
# Mayor que
print("¿Es", numero1, "mayor que", numero2, "?", numero1 > numero2)

# Menor que
print("¿Es", numero1, "menor que", numero2, "?", numero1 < numero2)

# Igualdad
print("¿Es", numero1, "igual a", numero2, "?", numero1 == numero2)

# Mayor o igual que
print("¿Es", numero1, "mayor o igual que", numero2, "?", numero1 >= numero2)

# Menor o igual que
print("¿Es", numero1, "menor o igual que", numero2, "?", numero1 <= numero2)

numero1 = 10
numero2 = 5
# Distinto: != y <>
print("¿Es", numero1, "diferente", numero2, "?", numero1 != numero2)

